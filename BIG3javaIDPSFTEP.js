function updatePlayerWeights(playerValues) {
    const maxWeight = 1.9; // Adjusted maximum weight
    const minWeight = 0.6; // Adjusted minimum weight
    const maxValue = 9994;
    const minValue = 500;

    for (const playerName in playerValues) {
        const player = playerValues[playerName];
        const value = player.value;

        if (value >= maxValue) {
            player.weight = maxWeight;
        } else if (value <= minValue) {
            player.weight = minWeight;
        } else {
            // Calculate weight based on a gradual scale
            const weight = ((maxWeight - minWeight) / (maxValue - minValue)) * (value - minValue) + minWeight;
            player.weight = parseFloat(weight.toFixed(2)); // Round to 2 decimal places
        }
    }
}
