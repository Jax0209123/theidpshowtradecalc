function suggestAssetsToAdd() {
    var tradeDifference = Math.abs(calculateTeamTotal(team1Trade) - calculateTeamTotal(team2Trade));
    var suggestedAssets = new Set();

    // Loop through playerValues to find assets that can be added to even the trade
    for (var playerName in playerValues) {
        if (!team1Trade.includes(playerName) && !team2Trade.includes(playerName)) {
            var player = playerValues[playerName];
            var playerValue = player.value;

            // Check if adding this player would bring the trade closer to even
            if (Math.abs(playerValue - tradeDifference) <= maxVariance * playerValue) {
                suggestedAssets.add(playerName);
            }
        }
    }

    // If the trade is already fair, return an empty array
    if (tradeDifference <= minRequiredValue) {
        return [];
    }

    // Loop through draftPicks to find assets that can be added to even the trade
    for (var pickName in draftPicks) {
        var pick = draftPicks[pickName];
        var pickValue = pick.value;

        // Check if adding this pick would bring the trade closer to even
        if (Math.abs(pickValue - tradeDifference) <= maxVariance * pickValue) {
            suggestedAssets.add(pickName);
        }
    }

    // Convert Set to array and shuffle it for randomness
    var shuffledAssets = Array.from(suggestedAssets).sort(() => Math.random() - 0.5);

    // Take up to 3 suggestions
    return shuffledAssets.slice(0, 3);
}



